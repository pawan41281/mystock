import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
public class JwtAuthResponse {
	private String accessToken;
	@JsonIgnore
	private String refreshToken;
	private String tokenType = "Bearer";
}